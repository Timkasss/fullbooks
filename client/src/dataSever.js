export async function author() {
   try {
      const authorPromises = writers.map(async (item) => {
         const formData = new FormData();
         let image = await fetch(item.image);
         let blob = await image.blob();
         formData.append('fullname', item.name);
         formData.append('birthday', item.birthDate);
         formData.append('placeofbirth', item.birthPlace);
         formData.append('deathdate', item.deathDate);
         formData.append('placeofdeath', item.deathPlace);
         formData.append('typeactivity', item.occupation);
         formData.append('direction', item.literaryMovement);
         formData.append('genre', item.genre);
         formData.append('languageworks', item.language);
         formData.append('image', blob);

         let response = await fetch('http://localhost:4000/authors', {
            method: 'POST',
            body: formData
         });

         if (response.ok) {
            console.log('Данные об авторах успешно отправлены');
         } else {
            console.error('Ошибка при отправке данных об авторах:', response.status, response.statusText);
         }
      });

      await Promise.all(authorPromises);
   } catch (error) {
      console.error('Произошла ошибка:', error);
   }
}
